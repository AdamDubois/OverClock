#define DATA_PIN     9
#define WIDTH        32
#define HEIGHT       8
#define NUM_LEDS     (WIDTH * HEIGHT)
CRGB leds[NUM_LEDS];

uint16_t XY(uint8_t x, uint8_t y) {
  if (x >= WIDTH || y >= HEIGHT) return 0;
  uint16_t index;
  if (x % 2 == 0) {
    index = (x * HEIGHT) + y;
  } else {
    index = (x * HEIGHT) + (HEIGHT - 1 - y);
  }
  return index;
}

